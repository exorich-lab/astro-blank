import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import siteConfigRaw from '../site.config.json' with { type: 'json' };

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');
const envPath = path.join(projectRoot, '.env.local');

const args = process.argv.slice(2);

const getArgValue = (name, fallback = '') => {
  const withEquals = args.find((item) => item.startsWith(`${name}=`));
  if (withEquals) {
    return withEquals.slice(name.length + 1).trim();
  }

  const index = args.indexOf(name);
  if (index >= 0 && args[index + 1]) {
    return args[index + 1].trim();
  }

  return fallback;
};

const hasArg = (name) => args.includes(name);

const parseBoolean = (value, fallback = false) => {
  if (typeof value !== 'string') {
    return fallback;
  }

  const normalized = value.trim().toLowerCase();
  if (['1', 'true', 'yes', 'y', 'on'].includes(normalized)) {
    return true;
  }

  if (['0', 'false', 'no', 'off', 'n'].includes(normalized)) {
    return false;
  }

  return fallback;
};

const toOneLine = (value) => String(value || '').trim();

const gtmId = getArgValue('--gtm-id', process.env.PUBLIC_GTM_ID || '').trim()
  || toOneLine(siteConfigRaw?.analytics?.gtmId || '');
const ga4Id = getArgValue('--ga4-id', '').trim();
const overwrite = hasArg('--force');
const dryRun = hasArg('--dry-run') || parseBoolean(process.env.GTM_SETUP_DRY_RUN, false);

if (!gtmId) {
  console.log('[gtm-setup] No GTM ID found.');
  console.log('[gtm-setup] Do one of the following:');
  console.log('[gtm-setup] 1) set analytics.gtmId in site.config.json');
  console.log('[gtm-setup] 2) pass --gtm-id GTM-XXXXXX');
  console.log('[gtm-setup] 3) set PUBLIC_GTM_ID in .env.local or shell env');
  console.log('[gtm-setup] Then rerun: npm run analytics:gtm:setup -- --gtm-id GTM-XXXXXX');
  process.exit(1);
}

const readEnv = () => {
  if (!fs.existsSync(envPath)) {
    return '';
  }

  return fs.readFileSync(envPath, 'utf8');
};

const parseEnv = (content) => {
  const output = {};

  for (const rawLine of content.split('\n')) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) {
      continue;
    }

    const eqIndex = line.indexOf('=');
    if (eqIndex <= 0) {
      continue;
    }

    const key = line.slice(0, eqIndex).trim();
    let value = line.slice(eqIndex + 1).trim();

    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }

    output[key] = value;
  }

  return output;
};

const writeEnv = (env) => {
  const existing = parseEnv(readEnv());

  const next = {
    ...env,
    PUBLIC_GTM_ID: gtmId,
  };

  if (ga4Id) {
    next.PUBLIC_GA4_ENABLED = 'true';
    next.PUBLIC_GA4_MEASUREMENT_ID = ga4Id;
  } else {
    delete next.PUBLIC_GA4_ENABLED;
    delete next.PUBLIC_GA4_MEASUREMENT_ID;
  }

  const kept = Object.entries(existing)
    .filter(([key]) => !['PUBLIC_GTM_ID', 'PUBLIC_GA4_ENABLED', 'PUBLIC_GA4_MEASUREMENT_ID'].includes(key))
    .map(([key, value]) => `${key}=${value}`);

  const header = ['# Auto-generated by npm run analytics:gtm:setup'];
  const generated = [
    `PUBLIC_GTM_ID=${next.PUBLIC_GTM_ID}`,
    ...(ga4Id ? [`PUBLIC_GA4_ENABLED=${next.PUBLIC_GA4_ENABLED}`, `PUBLIC_GA4_MEASUREMENT_ID=${next.PUBLIC_GA4_MEASUREMENT_ID}`] : []),
  ];

  const content = [...header, ...generated, ...kept].join('\n') + '\n';
  fs.writeFileSync(envPath, content, 'utf8');
};

const existing = parseEnv(readEnv());
if (!overwrite && existing.PUBLIC_GTM_ID && existing.PUBLIC_GTM_ID !== gtmId) {
  console.log('[gtm-setup] PUBLIC_GTM_ID already exists in .env.local.');
  console.log('[gtm-setup] Use --force to overwrite.');
  process.exit(1);
}

const printChecklist = () => {
  console.log('Minimal GTM setup now:');
  console.log('1) In GTM create GA4 Configuration tag');
  console.log('2) Use measurement ID: ' + (ga4Id || '<PASTE_GA4_ID>'));
  console.log('3) Trigger: All Pages');
  console.log('4) Add a Send Custom Event tag for business events if needed (track by dataLayer)');
  console.log('5) Publish container and test with Preview mode');
  console.log("   Example JS: import { pushAnalyticsEvent } from '@/lib/analytics'; pushAnalyticsEvent('cta_click', {event_category:'engagement'})");
  console.log('6) Use source code from /src/lib/analytics.ts for simple client tracking helpers.');
};

const apply = (isDryRunMode) => {
  if (isDryRunMode) {
    console.log('[gtm-setup] Dry run. No changes were written.');
    console.log(`PUBLIC_GTM_ID=${gtmId}`);
    if (ga4Id) {
      console.log('PUBLIC_GA4_ENABLED=true');
      console.log(`PUBLIC_GA4_MEASUREMENT_ID=${ga4Id}`);
    }
    printChecklist();
    return;
  }

  writeEnv(existing);
  console.log('[gtm-setup] Updated .env.local with analytics variables.');
  console.log(`PUBLIC_GTM_ID=${gtmId}`);
  if (ga4Id) {
    console.log(`PUBLIC_GA4_MEASUREMENT_ID=${ga4Id}`);
  }
  printChecklist();
};

apply(dryRun);
